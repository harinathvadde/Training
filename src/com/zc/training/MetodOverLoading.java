package com.zc.training;

public class MetodOverLoading {

	public static int add(int inputone, int inputtwo) {
		return inputone + inputtwo;
	}
	
	public static int add(int inputone, int inputtwo, int inputthree) {
		return inputone + inputtwo + inputthree;
	}
	
	public static int add(int inputone, int inputtwo, int inputthree, int inputfour) {
		return inputone + inputtwo + inputthree + inputfour;
	}
	
}
